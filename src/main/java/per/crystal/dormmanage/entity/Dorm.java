package per.crystal.dormmanage.entity;

import java.io.Serializable;

/**
 * @author Crystal
 * 2018/02/10
 * 宿舍表实体
 */
public class Dorm extends BaseEntity implements Serializable {

    private String dormNo;
    private Integer dormCapacity;
    private Integer dormNum;
    private String buildId;
    private Integer isDelete;
    private DormBuild dormBuild;
    private String buildName;

    public String getDormNo() {
        return dormNo;
    }

    public void setDormNo(String dormNo) {
        this.dormNo = dormNo;
    }

    public Integer getDormCapacity() {
        return dormCapacity;
    }

    public void setDormCapacity(Integer dormCapacity) {
        this.dormCapacity = dormCapacity;
    }

    public Integer getDormNum() {
        return dormNum;
    }

    public void setDormNum(Integer dormNum) {
        this.dormNum = dormNum;
    }

    public String getBuildId() {
        return buildId;
    }

    public void setBuildId(String buildId) {
        this.buildId = buildId;
    }

    public Integer getIsDelete() {
        return isDelete;
    }

    public void setIsDelete(Integer isDelete) {
        this.isDelete = isDelete;
    }

	public DormBuild getDormBuild() {
		return dormBuild;
	}

	public void setDormBuild(DormBuild dormBuild) {
		this.dormBuild = dormBuild;
	}

	public String getBuildName() {
		return buildName;
	}

	public void setBuildName(String buildName) {
		this.buildName = buildName;
	}

	
}
